int		ft_strncpy(char *dst, const char *src, unsigned int k)
{
	unsigned int j;

	j = 0;
	while (src[j] != '\0' && j < k)
	{
		dst[j] = src[j];
		j++;
	}
	dst[j] = '\0';
	return (dst);
}
