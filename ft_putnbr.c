#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483647)
		ft_putstr("-2147483647");
	else if (n < 0)
	{
		ft_putchar('-');
		ft_putnbr(-n);
	}
	else if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		ft_putnbr(n + '0');
}
