#include "libft.h"

char	*ft_strtrim(char const *s)
{
	unsigned int	p;
	size_t			jp;

	jp = 0;
	p = 0;
	if (!s)
		return (0);
	while (s[p] == ' ' || s[p] == '\n' || s[p] == '\t')
		p++;
	while (s[p] != '\0' || s[p] != ' ' || s[p] != '\n' || s[p] != '\t')
		jp++;
	return (ft_strsub(s, p, jp));
}
